//membuat formula keliling persegi, luas lingkaran, volume balok

void main() {
  print('keliling persegi:');
  int s = 4;
  var keliling = 4 * s;
  print(keliling);

  var r = 14;
  var luasLing = 3.14 * r * r;
  print('Luas Lingkaran');
  print(luasLing);

  int p = 10;
  int l = 10;
  int t = 100;
  print('Volume balok: ');
  var volume = p * l * t;
  print(volume);
}
